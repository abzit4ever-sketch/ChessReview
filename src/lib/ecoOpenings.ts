// Sort: longest move sequences first for best-match
ECO_DB.sort((a, b) => b[0].split(' ').length - a[0].split(' ').length);

/**
 * Given a list of UCI moves played so far, return the best matching opening name.
 */
export function lookupOpening(uciMoves: string[]): string | undefined {
  if (uciMoves.length === 0) return undefined;
  const movesStr = uciMoves.join(' ');
  for (const [line, name] of ECO_DB) {
    if (movesStr.startsWith(line)) return name;
  }
  return undefined;
}
